function limSP() {
    let a = 0;
    if (a == 0) {
        a = 1;
        let elem = document.getElementById('loadbar');
        let width = 1;
        let id = setInterval(frame, 10);

        function frame() {
            if (width >= 100) {
                clearInterval(id);
                a = 0;
                window.location.assign('../profiles/limSP.html');
            } else {
                width++;
                elem.style.width = width + "%";
                elem.innerHTML = "Loading... " + width + "%";
            }
        }
    }
}

